package com.giovani.calculadora;

public class Calculadora {
	
	public int somar(int n1,int n2) {
		
		return n1 + n2;
	}
	
	public int diminuir(int n1,int n2) {
		
		return n1 - n2;
	}
	
	public int multiplicacao(int n1,int n2) {
		
		return n1 * n2;
	}

	public float divisao(int n1,int n2) {
		
		return n1 / n2;
	}

}
